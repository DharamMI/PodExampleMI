import Foundation


public final class PodExampleMI {

    let name = "PodExampleMI"
    
    public func add(a: Int, b: Int) -> Int {
        return a + b
    }
    
    public func sub(a: Int, b: Int) -> Int {
        return a - b
    }
    
    public func multiple(a: Int, b: Int) -> Int {
        return a * b
    }
    
    public func division(a: Int, b: Int) -> Int  {
        return a / b
    }
    
}
